void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance==huart4.Instance)
	{
		HAL_GPIO_TogglePin(GPIOD ,GPIO_PIN_13);
		HAL_GPIO_TogglePin(GPIOD ,GPIO_PIN_15);		
		//compute value of mode, config and set_point
		mode = parameter_compute(0, data);
		config = parameter_compute(5, data);		
		set_point = parameter_compute(25, data);
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance == htim1.Instance)
	{
		if(config == 000)
		{
			uk = PID_compute(set_point, position, Kp, Ki, Kd);
		}
		else if(config == 111)
		{
			uk = 0;
		}
		else if(config == 001)
		{
			uk = 0;
			count = 0;
			__HAL_TIM_GET_COUNTER(&htim2) = 0;		
		}
	}
}

float parameter_compute(char i, uint8_t *data)
{
	float value = (data[0+i]-48)*100 + (data[1+i]-48)*10 + (data[2+i]-48) + (float)(data[3+i]-48)/10 + (float)(data[4+i]-48)/100;
	return value;
}

float PID_compute(float ref, float yk, float kp, float ki, float kd)
{
	static float uk,uk1,ek,ek1,ek2, Tcs = 0.01;
	ek = ref - yk;
	uk = uk1 + kp*(ek - ek1) + ki*Tcs*(ek + ek1)/2 + kd*(ek - 2*ek1 + ek2)/Tcs;    
	if(uk>999) uk = 999;
	if(uk<-999) uk = -999;
	uk1 = uk;
  ek2 = ek1;
  ek1 = ek;
	return uk;
}

void PWM_generate(float value)
{
		Value_PWM = (int)(value);
		if(Value_PWM > 0)
		{	
			__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,Value_PWM);
			__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2,0);
		}
		else if(Value_PWM < 0)
		{	
			__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,0);
			__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2,-Value_PWM);
		}
		else
		{
			__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,0);
			__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2,0);
		}
}


